#Ejemplo de programa en Python que suma dos numeros
number_1 = input('Por favor ingrese el primer numero \n')
number_2 = input('Por favor inserte el segundo numero \n')

#Uso del operador de suma + para sumar dos numeros.
sum = float(number_1) + float(number_2)
print('La suma de {0} and {1} is {2}'.format(number_1, number_2, sum))
